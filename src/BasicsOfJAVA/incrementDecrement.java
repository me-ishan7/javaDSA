package BasicsOfJAVA;

public class incrementDecrement {
    public static void main(String[] args) {
        int a = 5;
        a++;
        System.out.println(a);
        a--;
        System.out.println(a);
        ++a;
        System.out.println(a);
        --a;
        System.out.println(a);

        int b = 8;
        System.out.println(b++);
        System.out.println(b);

        System.out.println(++b);
        System.out.println(b);
    }
}
