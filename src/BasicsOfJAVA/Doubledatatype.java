package BasicsOfJAVA;

public class Doubledatatype {
    public static void main(String[] args) {
        double x = 5;
        double y = 2;
        double d = x/y;
        System.out.println(d);
        double d1 = 5/2;
        System.out.println(d1);
        double d2 = 5.0/2;
        System.out.println(d2);
    }
}
